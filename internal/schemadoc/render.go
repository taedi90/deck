package schemadoc

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

type PageInput struct {
	Family      string
	PageSlug    string
	Title       string
	Summary     string
	Description string
	Variants    []VariantInput
}

type VariantInput struct {
	Kind        string
	Title       string
	Description string
	SchemaPath  string
	Schema      map[string]any
	Meta        ToolMetadata
	Required    []string
	Spec        map[string]any
	Outputs     []string
	DocsOrder   int
}

type Field struct {
	Path        string
	Type        string
	Required    bool
	Default     string
	Enum        string
	Description string
	Example     string
}

type PageOverrides struct {
	FieldDocs map[string]FieldDoc
	Example   string
	Notes     []string
}

func RenderToolPage(in PageInput) []byte {
	var buf bytes.Buffer
	buf.WriteString("<!-- Code generated by make generate; DO NOT EDIT. -->\n")
	title := firstNonEmpty(in.Title, familyTitleFallback(in.Family))
	summary := firstNonEmpty(in.Summary, in.Description, "Reference for typed workflow steps in this family.")
	buf.WriteString("# " + title + "\n\n")
	buf.WriteString(summary + "\n\n")
	buf.WriteString("## Summary\n\n")
	buf.WriteString("- family: `" + in.Family + "`\n")
	if len(in.Variants) > 0 {
		buf.WriteString("- kinds: `" + strings.Join(pageKinds(in.Variants), "`, `") + "`\n")
	}
	buf.WriteString("\n## Shared Step Fields\n\n")
	buf.WriteString("Shared step envelope fields such as `id`, `apiVersion`, `kind`, `when`, `retry`, `timeout`, `register`, and `metadata` are documented in [Workflow Schema](../workflow.md).\n")
	if len(in.Variants) > 1 {
		buf.WriteString("\n## Supported Kinds\n\n")
		for _, variant := range sortedVariants(in.Variants) {
			buf.WriteString("- `" + variant.Kind + "`: " + firstNonEmpty(variant.Meta.Summary, variant.Description) + "\n")
		}
	}
	for _, variant := range sortedVariants(in.Variants) {
		fields := CollectFields(variant.Schema)
		applyFieldDocs(fields, variant.Meta.FieldDocs)
		rules := ExtractRules(variant.Spec, "spec")
		buf.WriteString("\n## `" + variant.Kind + "`\n\n")
		buf.WriteString(firstNonEmpty(variant.Meta.Summary, variant.Description) + "\n\n")
		buf.WriteString("- schema: `../../../" + variant.SchemaPath + "`\n")
		if len(variant.Outputs) > 0 {
			buf.WriteString("- outputs: `" + strings.Join(variant.Outputs, "`, `") + "`\n")
		}
		if strings.TrimSpace(variant.Meta.WhenToUse) != "" {
			buf.WriteString("\n### When To Use\n\n" + variant.Meta.WhenToUse + "\n")
		}
		buf.WriteString("\n### Example\n\n```yaml\n")
		example, err := toolExample(variant)
		if err != nil {
			buf.WriteString("# error: unable to render example\n")
			buf.WriteString("```\n")
			return buf.Bytes()
		}
		buf.WriteString(example)
		buf.WriteString("```\n")
		buf.WriteString("\n### Spec Fields\n\n")
		buf.WriteString(renderFieldTable(directChildFields(fields, "spec")))
		if nested := renderNestedSections(fields, "spec"); nested != "" {
			buf.WriteString("\n### Nested Objects\n\n")
			buf.WriteString(nested)
		}
		if len(rules) > 0 {
			buf.WriteString("\n### Validation Rules\n\n")
			for _, rule := range rules {
				buf.WriteString("- " + rule + "\n")
			}
		}
		if len(variant.Meta.Notes) > 0 {
			buf.WriteString("\n### Notes\n\n")
			for _, note := range variant.Meta.Notes {
				buf.WriteString("- " + note + "\n")
			}
		}
	}
	buf.WriteString("\n## Related\n\n")
	buf.WriteString("- [Schema Reference](../README.md)\n")
	return buf.Bytes()
}

